#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*arg;
	size_t	i;

	arg = b;
	i = 0;
	while (i < len)
	{
		*(arg + i) = c;
		i = i + 1;
	}
	return (b);
}
